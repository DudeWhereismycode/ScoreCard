public class Holes {
private Sting mLabel;
private int mStrokeCount;

public Holes (String Label, int strokeCount) {
    mLabel = label;
    mStrokeCount = strokeCount;
    }

    public Sting getmLabel() {
        return mLabel;
    }

    public void setmLabel(Sting mLabel) {
        this.mLabel = mLabel;
    }

    public int getmStrokeCount() {
        return mStrokeCount;
    }

    public void setmStrokeCount(int mStrokeCount) {
        this.mStrokeCount = mStrokeCount;
    }
}
